#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	i = 0;
	while (s[i])
	{
		(*f)(i, &s[i]);
		i++;
	}
}
/*
void ft_striteri(char *s, void (*f)(unsigned int, char*))
This function applies given function 'f' to each character in
the given string 's'.
Returns nothing.
*/
