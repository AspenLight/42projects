#include "libft.h"

int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
	{
		return (1);
	}
	return (0);
}
/*
int ft_isascii(int c)
This function takes a character as an integer holding its ASCII value,
then returns 1 if the character is on the standard ASCII table.
Otherwise returns 0.
*/
