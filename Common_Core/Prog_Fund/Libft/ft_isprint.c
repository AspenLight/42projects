#include "libft.h"

int	ft_isprint(int c)
{
	if (c >= 32 && c <= 126)
	{
		return (1);
	}
	return (0);
}
/*
int ft_isprint(int c)
This function takes a character as an integer holding its ASCII value,
then returns 1 if the character is a printable character.
Otherwise returns 0.
*/
