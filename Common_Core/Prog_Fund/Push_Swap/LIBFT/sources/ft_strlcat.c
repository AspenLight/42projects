#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	srclen;
	size_t	dstlen;

	srclen = ft_strlen(src);
	dstlen = ft_strlen(dst);
	if (dstlen >= size)
		return (size + srclen);
	dst += dstlen;
	while (*src && --size > dstlen)
		*dst++ = *src++;
	*dst = '\0';
	return (srclen + dstlen);
}
