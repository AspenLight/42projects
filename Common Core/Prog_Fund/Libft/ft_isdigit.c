#include "libft.h"

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
	{
		return (1);
	}
	return (0);
}
/*
int ft_isdigit(int c)
This function takes a character as an integer holding its ASCII value,
then returns 1 if the character is a number.
Otherwise returns 0.
*/
