#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
	{
		return (1);
	}
	return (0);
}
/*
int ft_isalpha(int c)
This function takes a character as an integer holding its ASCII value,
then returns 1 if the character is a letter.
Otherwise returns 0.
*/
