#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_content;
	t_list	*new_node;

	if (!lst || !f || !del)
		return (NULL);
	new_list = NULL;
	while (lst)
	{
		new_content = f(lst->content);
		new_node = ft_lstnew(new_content);
		if (!new_node)
		{
			if (del != NULL)
				del(new_content);
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_node);
		lst = lst->next;
	}
	return (new_list);
}
/*
t_list *ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
This function uses the custom data structure t_list, found in libft.h
This function iterates through given list 'lst'
and creates a new list containing the results
of applying given function 'f' to the content of each node in 'lst'
Returns the new list.
*/
