#include "libft.h"

char	*ft_strchr(const char *s, char c)
{
	unsigned int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i++;
	}
	if (s[i] == (char)c)
		return ((char *)&s[i]);
	return (NULL);
}
/*
char *ft_strchr(const char *s, char c)
This function finds the first occurence of given character 'c'
within given string 's' and returns a pointer to the character
within the string.
If no matching character is found, returns NULL.
*/
