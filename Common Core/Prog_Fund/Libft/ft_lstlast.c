#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*temp;

	if (!lst)
		return (NULL);
	temp = lst;
	while (temp->next)
		temp = temp->next;
	return (temp);
}
/*
t_list *ft_lstlast(t_list *lst)
This function uses the custom data structure t_list, found in libft.h
This function finds and returns the last node of given list 'lst'
*/
