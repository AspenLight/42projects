#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
	return ;
}
/*
void ft_putchar_fd(char c, int fd)
This function writes character 'c' to the
terminal or file pointed to by file descriptor 'fd'
Returns nothing.
*/
