#include <unistd.h>

void	ft_is_negative(int n)
{
	char	neg;

	if (n < 0)
	{
		neg = 'N';
		write(1, &neg, 1);
	}
	else
	{
		neg = 'P';
		write(1, &neg, 1);
	}
}
